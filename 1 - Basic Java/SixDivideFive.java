public class SixDivideFive {

	public static void main(String[] args) {

		System.out.println("6/5 = " + (6/5));
		System.out.println("6.0/5 = " + (6.0/5));
		System.out.println("6/5.0 = " + (6/5.0));
		System.out.println("6.0/5.0 = " + (6.0/5.0));


	}
}
